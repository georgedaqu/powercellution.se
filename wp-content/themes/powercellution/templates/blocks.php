					being where the fuel cell runs your site independently or connected to the grid. Peak power helps
					you compensate for lack of power at times of high demand. Back-up power is where the fuel cell
					system steps in to keep your operation going when there is a power failure.</p>
				<h5>Primary power</h5>
				<p>When you want to be more self-sufficient, to secure electricity access or simply work in places where
					there is no grid, you need your own power source. It could be that you want to decrease your grid
					dependency, lower grid costs or to set up temporary operations at an inaccessible spot.</p>
				<h5>Peak power</h5>
				<p>At times when the energy demand on the grid peaks, you will want to ensure that you have access to
					the power you need. A hydrogen fuel cell can either eliminate or complement other energy sources,
					such as batteries, to do the job. As a bonus, it will cut costs when energy prices rise as the
					demand increases.</p>
				<h5>Back-up power</h5>
				<p>With the rise of digitization, back-up power has become increasingly important. It has been customary
					to use diesel generators that start up when there is a power failure. A hydrogen fuel cell can
					always be online, which makes it more dependable and your power supply system more robust.</p>
